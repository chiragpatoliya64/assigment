#include<stdio.h>
main()
{
	int days;
	
	printf("enter days :");
	scanf("%d",&days);
	
	switch(days)
	{
		case 1:
			printf("\nsunday");
			break;
			
			case 2:
				printf("\nmonday");
				break;
				
				case 3:
					printf("\ntuesday");
					break;
					
					case 4:
						printf("\nwednesday");
						break;
						
						case 5:
							printf("\nthursday");
							break;
							
							case 6:
								printf("\nfriday");
								break;
								
								case 7:
									printf("\nsaturday");
									break;
									
							
	}
}
